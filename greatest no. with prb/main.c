#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a,b,c;
    printf("Enter number 1 :");
    scanf("%d/n",&a);
    printf("Enter number 2 :");
    scanf("%d/n",&b);
    printf("Enter number 3 :");
    scanf("%d/n",&c);
    if(a>b){
        if(a>c){
            printf("%d is the greatest number between 3",a);
        }
        else if(b>c){
            printf("%d is the greatest number between 3",b);
        }
        else{
            printf("%d is the greatest number between 3",c);
        }
    }
}
